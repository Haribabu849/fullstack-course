let day = "monday,tuesday";

// switch ===
// default similar to else
switch (day) {
  case "monday":
  case "tuesday":
  case "wednesday":
  case 2:
    console.log("css");
    break;
  case "friday":
    console.log("js");
    break;
  default:
    console.log("break");
    break;
}

